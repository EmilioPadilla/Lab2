DROP TABLE IF EXISTS `Materiales`;
CREATE TABLE `Materiales` (
  `Clave` decimal(5,0) NOT NULL,
  `Descripcion` varchar(50) DEFAULT NULL,
  `Costo` decimal(8,2) DEFAULT NULL,
  `PorcentajeImpuesto` decimal(6,2) DEFAULT NULL
